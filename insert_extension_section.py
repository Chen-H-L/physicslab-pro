def main():
    source = find_target_doc()
    output = source.with_name(source.stem + "_扩展内容已补充.docx")
    copy2(source, output)
    doc = Document(output)

    for p in doc.paragraphs:
        stripped = p.text.strip()
        new_text = replace_numbering(stripped)
        if new_text != stripped:
            p.text = p.text.replace(stripped, new_text, 1)

    anchor = None
    for p in doc.paragraphs:
        if p.text.strip().startswith("五、设计流程与实现技术"):
            anchor = p
            break
    if anchor is None:
        raise RuntimeError("未找到插入位置：五、设计流程与实现技术。")

    style_map = {"h1": "Heading 1", "h2": "Heading 2", "h3": "Heading 3", "p": "Normal"}
    for kind, text in SECTION_ITEMS:
        anchor.insert_paragraph_before(text, style=style_map[kind])

    doc.save(output)
    print(output)


if __name__ == "__main__":
    main()
