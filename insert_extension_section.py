def find_target_doc() -> Path:
    desktop = Path.home() / "Desktop"
    matches = [
        path
        for path in desktop.glob("*.docx")
        if not path.name.startswith("~$")
        and "AI" in path.name
        and "设计报告" in path.name
    ]
    if not matches:
        raise FileNotFoundError("未在桌面找到目标设计报告 docx。")
    return matches[0]


def replace_numbering(text: str) -> str:
    replacements = [
        ("七、总结、局限性与展望", "八、总结、局限性与展望"),
        ("六、运行环境与配置要求", "七、运行环境与配置要求"),
        ("五、创新特色", "六、创新特色"),
        ("四、设计流程与实现技术", "五、设计流程与实现技术"),
        ("7.", "8."),
        ("6.", "7."),
        ("5.", "6."),
        ("4.", "5."),
    ]
    for old, new in replacements:
        if text.startswith(old):
            return text.replace(old, new, 1)
    return text


